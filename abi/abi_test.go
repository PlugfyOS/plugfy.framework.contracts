// findModuleRoot walks up from the working directory to the directory holding
// go.mod, so the test works regardless of the directory `go test` runs it from.
func findModuleRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("go.mod not found from %s upward", dir)
		}
		dir = parent
	}
}

// unifiedDiff renders a minimal line-oriented diff (added/removed lines) to make
// surface drift legible in test output without pulling in an external diff
// library. It compares the two line multisets: a line present more often in
// want than in got is reported as removed (-), and vice versa as added (+). It
// is a presentation aid only; the equality decision is the exact string compare
// in the test body.
func unifiedDiff(want, got string) string {
	wantLines := strings.Split(want, "\n")
	gotLines := strings.Split(got, "\n")

	wantCount := map[string]int{}
	for _, l := range wantLines {
		wantCount[l]++
	}
	gotCount := map[string]int{}
	for _, l := range gotLines {
		gotCount[l]++
	}

	var removed []string
	for _, l := range wantLines {
		if wantCount[l] > gotCount[l] {
			removed = append(removed, l)
			wantCount[l]-- // count each surplus occurrence once
		}
	}
	var added []string
	for _, l := range gotLines {
		if gotCount[l] > wantCount[l] {
			added = append(added, l)
			gotCount[l]--
		}
	}

	var b strings.Builder
	b.WriteString("--- want (golden)\n+++ got (current)\n")
	for _, l := range removed {
		b.WriteString("- " + l + "\n")
	}
	for _, l := range added {
		b.WriteString("+ " + l + "\n")
	}
	if len(removed) == 0 && len(added) == 0 {
		b.WriteString("(line multiset identical; the difference is line order — re-run with -update if intentional)\n")
	}
	return b.String()
}
